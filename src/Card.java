public class Card {
    private String CardNumber;
    private String code;

    public void setCardNumber(String cardNumber) {
        CardNumber = cardNumber;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCardNumber() {
        return CardNumber;
    }

    public String getCode() {
        return code;
    }

}
